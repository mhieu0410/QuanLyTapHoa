    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try {
        // Kiểm tra đầu vào không được để trống
        if (txtName.getText().isEmpty() || txtCategory.getText().isEmpty()
                || txtImportPrice.getText().isEmpty() || txtSellPrice.getText().isEmpty()
                || txtQuantity.getText().isEmpty() || txtExpiryDate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        // Tạo đối tượng Product và gán giá trị
        Product p = new Product();
        p.setName(txtName.getText());
        p.setCategory(txtCategory.getText());
        p.setImportPrice(Double.parseDouble(txtImportPrice.getText()));
        p.setSellPrice(Double.parseDouble(txtSellPrice.getText()));
        p.setQuantity(Integer.parseInt(txtQuantity.getText()));

        // Chuyển đổi chuỗi sang java.sql.Date nếu dùng SQL
        try {
            String dateStr = txtExpiryDate.getText(); // "yyyy-MM-dd"
            java.sql.Date sqlDate = java.sql.Date.valueOf(dateStr);
            p.setExpiryDate(txtExpiryDate.getText()); // bỏ đoạn sqlDate
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Hạn sử dụng không hợp lệ! Định dạng đúng: yyyy-MM-dd");
            return;
        }

        // Thêm vào DB
        if (ProductDAO.addProduct(p)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            loadTable(); // Cập nhật lại JTable
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Giá hoặc số lượng phải là số hợp lệ!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi nhập dữ liệu: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed
    
