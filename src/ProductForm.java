    private void loadTable() {
    DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
    model.setRowCount(0); // Xóa bảng cũ nếu có

    for (Product p : ProductDAO.getAllProducts()) {
        model.addRow(new Object[]{
            p.getId(),
            p.getName(),
            p.getCategory(),
            p.getImportPrice(),
            p.getSellPrice(),
            p.getQuantity(),
            p.getExpiryDate()
        });
    }
}
    
    


